use std::fs;
use std::io::ErrorKind;
use std::path::Path;

fn ensure_cable_icon() -> Result<(), Box<dyn std::error::Error>> {
    if !icon_is_valid(CABLE_ICON) {
        panic!("embedded cable icon payload is corrupt");
    }

    let icon_path = Path::new("icons").join("icon.ico");
    let should_write = match fs::read(&icon_path) {
        Ok(existing) => {
            if existing == CABLE_ICON {
                false
            } else if existing == LEGACY_PLACEHOLDER_ICO {
                true
            } else if !icon_is_valid(&existing) {
                true
            } else {
                false
            }
        }
        Err(err) => match err.kind() {
            ErrorKind::NotFound => true,
            _ => return Err(err.into()),
        },
    };

    if should_write {
        if let Some(parent) = icon_path.parent() {
            fs::create_dir_all(parent)?;
        }
        fs::write(&icon_path, CABLE_ICON)?;
    }

    Ok(())
}

fn icon_is_valid(bytes: &[u8]) -> bool {
    if bytes.len() < 6 {
        return false;
    }

    if bytes[0] != 0 || bytes[1] != 0 || bytes[2] != 1 || bytes[3] != 0 {
        return false;
    }

    let count = u16::from_le_bytes([bytes[4], bytes[5]]) as usize;
    if count == 0 {
        return false;
    }

    let dir_bytes = 6 + (count * 16);
    if bytes.len() < dir_bytes {
        return false;
    }

    let mut offset = 6;
    for _ in 0..count {
        let entry = &bytes[offset..offset + 16];
        let size = u32::from_le_bytes([entry[8], entry[9], entry[10], entry[11]]) as usize;
        let image_offset =
            u32::from_le_bytes([entry[12], entry[13], entry[14], entry[15]]) as usize;
        if size == 0 {
            return false;
        }
        if image_offset < dir_bytes {
            return false;
        }
        if image_offset + size > bytes.len() {
            return false;
        }
        offset += 16;
    }

    true
}

fn main() {
    ensure_cable_icon().expect("failed to prepare Windows icon for Tauri build");
    tauri_build::build()
}

